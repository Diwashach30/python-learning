day_num = int(input("Enter Day number : "))

if day_num == 1:
    print ("Day is Sunday")
elif day_num == 2 :
    print ("Day is Monday")
elif day_num == 3:
    print ("Day is Tuesday")
elif day_num == 4 :
    print ("Day is Wednesday")
elif day_num == 5 :
    print ("Day is Thrusday")
elif day_num == 6 :
    print ("Day is Friday")
elif day_num == 7 :
    print ("Day is Saturday")
else :
    print ("Invalid Day")